package core;

public class Calculator {

	
	
	// test_01_add_2
	static double add(double a, double b) 
		{return a + b;}

	// test_02_add_3
	static double add(double a, double b, double c) 
		{return a + b + c;}
	
	// test_03_add_4
	static double add(double a, double b, double c, double d) 
		{return a + b + c + d;}

	// test_04_divide_2
	static double divide(double a, double b) 
	{return a / b;}
	
	// test_05_divide_3
	static double divide(double a, double b, double c) 
		{return a / b / c;}
	
	// test_06_divide_4
	static double divide(double a, double b, double c, double d) 
		{return a / b / c / d;}
	
	// test_07_multiply_2
	static double multiply(double a, double b) 
		{return a * b;}
	
	// test_08_multiply_3
	static double multiply(double a, double b, double c) 
		{return a * b * c;}
	
	// test_09_multiply_4
	static double multiply(double a, double b, double c, double d) 
		{return a * b * c * d;}
	
	
	
	// test_10_subtract_2
	static double subtract(double a, double b) 
		{return a - b;}
		
	// test_11_subtract_3	
	static double subtract(double a, double b, double c) 
		{return a - b - c;}
		
	// test_12_subtract_4	
	static double subtract(double a, double b, double c, double d) 
		{return a - b - c - d;}
	
	
	
	
	
}
